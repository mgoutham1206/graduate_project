package edu.nwmissouri.geoapp.controller.form;

public class Phase2Form {

	private String luster;
	private Integer hardness;
	private String streakColor;
	private String cleavage;
	private Integer sets;
	private Integer angle;
	private Integer specificGravity;
	private String color;
	private String name;

	public String getLuster() {
		return luster;
	}

	public void setLuster(String luster) {
		this.luster = luster;
	}

	/**
	 * @return the hardness
	 */
	public Integer getHardness() {
		return hardness;
	}

	/**
	 * @param hardness
	 *            the hardness to set
	 */
	public void setHardness(Integer hardness) {
		this.hardness = hardness;
	}

	/**
	 * @return the streakColor
	 */
	public String getStreakColor() {
		return streakColor;
	}

	/**
	 * @param streakColor
	 *            the streakColor to set
	 */
	public void setStreakColor(String streakColor) {
		this.streakColor = streakColor;
	}

	/**
	 * @return the cleavage
	 */
	public String getCleavage() {
		return cleavage;
	}

	/**
	 * @param cleavage
	 *            the cleavage to set
	 */
	public void setCleavage(String cleavage) {
		this.cleavage = cleavage;
	}

	/**
	 * @return the sets
	 */
	public Integer getSets() {
		return sets;
	}

	/**
	 * @param sets
	 *            the sets to set
	 */
	public void setSets(Integer sets) {
		this.sets = sets;
	}

	/**
	 * @return the angle
	 */
	public Integer getAngle() {
		return angle;
	}

	/**
	 * @param angle
	 *            the angle to set
	 */
	public void setAngle(Integer angle) {
		this.angle = angle;
	}

	/**
	 * @return the specificGravity
	 */
	public Integer getSpecificGravity() {
		return specificGravity;
	}

	/**
	 * @param specificGravity
	 *            the specificGravity to set
	 */
	public void setSpecificGravity(Integer specificGravity) {
		this.specificGravity = specificGravity;
	}

	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}

	/**
	 * @param color
	 *            the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
